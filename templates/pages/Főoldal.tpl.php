<h3>Mi a Rádió adó?</h3>
<p> A rádióadás lényege az információ továbbítása a térben vezeték nélkül, elektromágneses hullámok segítségével. A folyamat a stúdióban kezdődik. A mikrofon a hangrezgéseket elektromos jellé alakítja. Mivel ez a jel önmagában nem tudna nagy távolságra eljutni, „ráültetik” egy magas frekvenciájú hullámra, amit vivőhullámnak nevezünk. Ezt a folyamatot hívják modulációnak. AM (Amplitúdómoduláció) és FM (Frekvenciamoduláció) a két fő típusa.</p>
<h3>Honnan származik?</h3>
<p>James Clerk Maxwell matematikailag megjósolta, Heinrich Hertz pedig bizonyította az elektromágneses hullámok létezését. Guglielmo Marconi épített először nagy távolságra sugárzó rendszert, míg Nikola Tesla fejlesztette ki a nagyfrekvenciás tekercseket. Magyar vonatkozásban Puskás Tivadar a telefonhírmondóval a műsorszórás alapelvének megteremtője.</p>
